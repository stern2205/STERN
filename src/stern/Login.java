    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        // TODO add your handling code here (LOG IN BUTTON):
        
        String email1 = email.getText();
        String password = String.valueOf(this.password.getPassword());

        try {
            String sql = "SELECT * FROM USER WHERE Email = ? AND Password = ?";
            PreparedStatement stmt = con.prepareStatement(sql);

            stmt.setString(1, email1);
            stmt.setString(2, password);
            
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                
                Welcome WelcomeFrame = new Welcome();
                WelcomeFrame.setVisible(true);
                WelcomeFrame.pack();
                WelcomeFrame.setLocationRelativeTo(null);
                this.dispose();
                
            } else {
                UIManager.put("OptionPane.background", new Color (39,39,39));
                UIManager.put("Panel.background", new Color(69,69,69));
                UIManager.put("OptionPane.messageForeground", Color.WHITE);
                JOptionPane.showMessageDialog(null, "Invalid Email or Password","Login Error",2);
            }
            rs.close();
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_loginbtnActionPerformed

