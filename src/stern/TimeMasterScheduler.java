    public void startAlarm(){
        try {
            File file = new File("Alarm Clock - Beep Alarm for 1 Minute.mp3");
            String sound = file.getAbsolutePath();
            FileInputStream fileInputStream = new FileInputStream(sound);
            player = new Player(fileInputStream);

            // para malaman na nag riring na
            

            player.play();
        } catch (FileNotFoundException | JavaLayerException e) {
            System.out.println("Error playing MP3 file: " + e);
        }
    }
    public void stopAlarm(){
        if(player!=null){
            player.close();
        }
    }
    
    
