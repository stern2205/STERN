    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Timer timer = new Timer(); //timer ng tunog ng alarm
        Timer timer2 = new Timer(); // duration of the sound
    
        LocalTime timenow = LocalTime.now(); 

        if(jButton1.getText().equals("SET ALARM")){ //button

            //para malaman kung anong oras ang sinet na alarm
            set1.setText("Alarm Set: "+hour.getText()+":"+min.getText()+":"+sec.getText());
            set2.setText("");
            set3.setText("");

            //pagkuha ng values ng sinet na alarm
            // yung mga times 60 ay para sa timer, by seconds kase
            int hour1, hourD, min1, minD, sec1, duration, hours, mins, secs;
            hour1 = Integer.parseInt(hour.getText());
            
            hourD = hour1 * 60 * 60;
            min1 = Integer.parseInt(min.getText());
            minD = min1 * 60;
            sec1 = Integer.parseInt(sec.getText());

            //pagkuha ng values ng local time when the alarm is set
            hours = timenow.getHour()*60*60;
            mins = timenow.getMinute()*60;
            secs = timenow.getSecond();

            // eto duration ng timer
            //total seconds alarmset minus total seconds time na sinet ang alarm
            duration = (hourD + minD + sec1)- (hours + mins + secs);
            if (duration <= 0){
                        stopAlarm();
                        set2.setText("Already past");
                        set3.setText("that time");
                        set1.setText("");
            }else{
            System.out.println(duration);
            //eto na timer
            
            timer.schedule(new TimerTask() {
                int counter = 0;
                int counter2 = 0;
                
                
                
                @Override
                public void run() { 
                    System.out.println("Timer: " + counter++);
                    
                    if (counter > duration){
                        timer2.schedule(new TimerTask() {
                            @Override
                            public void run() {


                                System.out.println(counter2++);

                                if (counter2 >60){
                                    stopAlarm();
                                    timer.cancel();
                                    jButton1.setText("SET ALARM");
                                    set1.setText("");
                                    set2.setText("");
                                    set3.setText("");
                                    hour.setText("00");
                                    min.setText("00");
                                    sec.setText("00"); 
                                }
                                
                            }
                        }, 0, 1000);
                        jButton1.setText("STOP ALARM");
                        set1.setText("Alarm Ringing");
                        set2.setText("");
                        set3.setText("");
                        startAlarm();
                        timer.cancel();
                        }
                }
        
            }, 0, 1000); 
        }
        }
        else if (jButton1.getText().equals("STOP ALARM")){
            timer.cancel();
            timer2.cancel();
            stopAlarm();
            jButton1.setText("SET ALARM");
            set1.setText("");
            set2.setText("");
            set3.setText("");
            hour.setText("00");
            min.setText("00");
            sec.setText("00");
            
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

